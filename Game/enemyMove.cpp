//--------------------------------------------------------------------------------------
//  アクションをする関数
//--------------------------------------------------------------------------------------
void EnemyMove::Action( void )
{
	//  シーンクラスのポインタ
	Scene* pScene = NULL;			

	//  速度の代入
	GetEnemy( )->SetSpeed( GetEnemy( )->GetBaseSpeed( ) );

	//  座標の更新( 進行方向 × 速度 分の移動 )
	GetEnemy( )->MovePos( GetEnemy( )->GetVecDirect( ) * GetEnemy( )->GetSpeed( ) );

	//  注視点を向いている方向に
	GetEnemy( )->SetPositionAt( GetEnemy( )->GetPos( ) + GetEnemy( )->GetVecDirect( ) * 5.0f );
}