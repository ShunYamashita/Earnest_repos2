#include "layerAI.h"
#include "nodeAI.h"
#include <algorithm>

//--------------------------------------------------------------------------------------
//  マクロ定義
//--------------------------------------------------------------------------------------

//--------------------------------------------------------------------------------------
//  全てのノードのデータの破棄
//--------------------------------------------------------------------------------------
void LayerAI::Release( void )
{
	std::for_each( m_actionNode.begin( ) , m_actionNode.end( ) ,
				   [ ]( NodeAI* nodeAI )
				   {
						nodeAI->Release( );
				   } );
}

//--------------------------------------------------------------------------------------
//  全てのノードの実行可能状態の判断
//--------------------------------------------------------------------------------------
void LayerAI::AllJudgeActive( void )
{
	std::for_each( m_actionNode.begin( ) , m_actionNode.end( ) ,
				   [ ]( NodeAI* nodeAI )
				   {
						nodeAI->JudgeActive( );
				   } );
}