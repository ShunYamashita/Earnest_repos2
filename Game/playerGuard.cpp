{

}

//--------------------------------------------------------------------------------------
//  アクションをする関数
//--------------------------------------------------------------------------------------
void PlayerGuard::Action( void )
{
	//  キーが1か2の場合
	if( GetPlayer( )->GetAnimationKey( ) <= 2 )
	{
		//  防御状態に
		GetPlayer( )->SetGuardFlag( true );
	}
	else
	{
		//  防御解除状態に
		GetPlayer( )->SetGuardFlag( false );
	}

	//  アニメーションが終了している場合
	if( GetPlayer( )->GetAnimationFinish( ) )
	{
		//  モーションの変更
		GetPlayer( )->SetAnimation( StateAnimator::MOTION_NEUTRAL );

		//  状態の変更
		GetPlayer( )->ChangeState( GetPlayer( )->GetPlayerState( Player::STATE::NEUTRAL ) );

		//  エフェクシアの再生ストップ
		EffekseerManager::Stop( GetPlayer( )->GetEffekseerHandle( EffekseerManager::TYPE_SHIELD ) );
	}
}