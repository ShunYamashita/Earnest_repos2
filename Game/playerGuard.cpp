{
	//  親の初期化
	PlayerState::Init( );
}

//--------------------------------------------------------------------------------------
//  終了処理をする関数
//--------------------------------------------------------------------------------------
void PlayerGuard::Uninit( void )
{
	//  防御解除状態に
	GetPlayer( )->SetGuardFlag( false );
}

//--------------------------------------------------------------------------------------
//  更新処理をする関数
//--------------------------------------------------------------------------------------
void PlayerGuard::Update( void )
{
	//  カメラ制御
	ControlCamera( );

	//  アクション
	Action( );
}

